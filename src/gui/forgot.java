    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String companyname = companyname1.getText();
            String email = emaila.getText();
            String newpass = String.valueOf(newpassw.getPassword());
            String connewpass = String.valueOf(cnewpass.getPassword());
            if(!newpass.equals(connewpass)){
                JOptionPane.showMessageDialog(null, "Password does not match!");
            }
            else{
                
                int n  = database.dbclass.st.executeUpdate("Update user set password = '"+newpass+"'  where companyname = '"+companyname+"' and email = '"+email+"' ");
                if (n == 0){
                JOptionPane.showMessageDialog(null, "Company name and Email does not match");
                }
                else{
                   JOptionPane.showMessageDialog(null, "Password changed successfully"); 
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Company name and Email does not match");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

